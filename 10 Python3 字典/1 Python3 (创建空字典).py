# 使用大括号 {} 来创建空字典
emptyDict = {}

# 打印字典
print(emptyDict)

# 查看字典的数量
print("Length:", len(emptyDict))

# 查看类型
print(type(emptyDict))

# 输出结果

# {}
# Length: 0
# <class 'dict'>


# 使用内建函数 dict() 创建字典

mptyDict = dict()

# 打印字典
print(emptyDict)

# 查看字典的数量
print("Length:", len(emptyDict))

# 查看类型
print(type(emptyDict))

# 输出结果

# {}
# Length: 0
# <class 'dict'>
